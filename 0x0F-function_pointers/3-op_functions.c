#include <stdio.h>
#include "3-calc.h"
/**
* op_add-returns the sum
* @a: Int to sum
* @b: int to sum
* Return: result of sum
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
* op_sub- resta
* @a: Int to substract
* @b: int to substract
* Return: result of substract
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
* op_mul - returns the mult
* @a: Int to mult
* @b: int to mult
* Return: result of sum
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
* op_div - returns the div
* @a: Int to div
* @b: int to div
* Return: result of sum
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
* op_mod-modulo
* @a: Int to div
* @b: int to module
* Return: result of sum
*/
int op_mod(int a, int b)
{
	return (a % b);
}
